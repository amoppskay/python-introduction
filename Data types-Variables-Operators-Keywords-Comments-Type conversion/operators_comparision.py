# Comparison of Relational operators compares the values. It either returns True or False according to the condition.

# Examples of Relational Operators
a = 13
b = 33

# a > b is False
print(a > b)

# a < b is True
print(a < b)

# a == b is False
print(a == b)
# comparison; 13 is not equal to 33 hence it's true

# a != b is True
print(a != b)
#  not equal to

# a >= b is False
print(a >= b)
# a is greater/equal to b is false

# a <= b is True
print(a <= b)
# a is less than be b is true