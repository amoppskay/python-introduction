# To explain the python string data type
print('hello world')
print('hi there')
a = 'car'
print(a)
# declaring the value of a which = car

fruit_type = "banana"
print(fruit_type)
# declaring the value of fruit_type = banana

c = ''
# c is returning an 'empty space' as value since we didnt put any value/text in it
print(c)
print(len(c))
# we want to print length (len) hence it will count the no of letters that make up the value, here since its empty = 0

# # print data types using python keyword type
print(type(''))
print(type("hello world"))
print(type(a))
print(type(fruit_type))
print(type(c))
